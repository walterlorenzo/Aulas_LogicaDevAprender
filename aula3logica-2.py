# Laços de repetição + Listas
# Problemas 1 a N - imprime os valores 1 a N
'''
input numero maximo
valor inicial = 1
loop de valor_inicial a valor_maximo
    print valor
'''
valor_maximo = int(input("Digite o Valor máximo"))
valor_inicial = 1
for numero in range(valor_inicial,valor_maximo + 1):
    print(numero)

